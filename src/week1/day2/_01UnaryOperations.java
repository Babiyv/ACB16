package week1.day2;

public class _01UnaryOperations {
    public static void main(String[] args) {
        int i = 15;
        System.out.println(i);
        // take i -> i+1 -> rewrite i
        i = i + 1;

        i += 1;
        // i = i + 1;
        i *= 1;
        // i = 1*1;
        i -= 1;
        // i = i - 1;
        i /= 1;
        // i = i / 1;

        // i = i + 1;
        // rewrite i;

        i++;
        // but i + 1 don`t rewrite i;
        // if you want to rewrite use i = i + 1;



        System.out.println (i);
        System.out.println (i+1);

    }

}
