package week1.day2;

public class _05LogicalOperations {
    public static void main(String[] args) {
        boolean bool1 = false;
        boolean bool2 = true;

        // | - или (является ли один из переменных "true");
        // & - и (являются ли все переменные "true");
        // ^ - исключающее "или";
        // ! - не;
        // || - сокращенное "или";
        // && - скоращенное "и";
        // == - равен (равен ли один второму);
        // != - не равен (не равен ли один второму);

        boolean result = bool1 | bool2;
        System.out.println(result);







    }
}
