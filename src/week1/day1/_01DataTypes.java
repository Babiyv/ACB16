package week1.day1;

public class _01DataTypes {
    public static void main(String[] args) {
        // <data type> <variable name> = <data>;
        int age = -2147483648;
        System.out.println(age);

        byte b = 13;
        // L or l
        long l = 5888888888888L;

        short s = 13;
        // f or F
        float f = 25.7F;

        double d = 37.8;

        char letter = 'f';
        System.out.println(letter);

        boolean bool = true;
        boolean bool2 = false;
        System.out.println(bool);

    }


}
